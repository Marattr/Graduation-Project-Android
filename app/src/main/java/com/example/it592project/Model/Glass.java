    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public boolean isAntirefle() {
        return antirefle;
    }

    public void setAntirefle(boolean antirefle) {
        this.antirefle = antirefle;
    }

